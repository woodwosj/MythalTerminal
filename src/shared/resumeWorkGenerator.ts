export interface ProjectState {
  name: string;
  path: string;
  lastUpdated: Date;
  totalTokens: number;
  contextLayers: Array<{
    id: number;
    type: 'core' | 'active' | 'reference' | 'archive';
    content: string;
    tokens: number;
    isStarred: boolean;
    createdAt: Date;
    lastAccessed?: Date;
  }>;
  recentDecisions: Array<{
    id: number;
    summary: string;
    rationale: string;
    tags: string[];
    createdAt: Date;
  }>;
  activeProgress: Array<{
    id: number;
    description: string;
    status: string;
    createdAt: Date;
  }>;
  systemPatterns: Array<{
    name: string;
    description: string;
    tags: string[];
  }>;
  testResults?: {
    unit: { total: number; passed: number; coverage: string };
    e2e: { total: number; passed: number; duration: string };
    lastRun: Date;
  };
}

export interface ConPortData {
  decisions: any[];
  progress: any[];
  patterns: any[];
  customData: any[];
  recentActivity: any[];
}

